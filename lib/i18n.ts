const STORE_LANGUAGE_KEY = 'settings.lang';

i18n
    .use(initReactI18next)
    .init({
        resources,
        fallbackLng: 'en',
        lng: 'en',
        interpolation: {
            escapeValue: false,
        },
        react: {
            useSuspense: false,
        },
    });

export function changeAppLanguage(lng: string) {
    console.log('[i18n] changeAppLanguage called with:', lng);
    i18n.changeLanguage(lng);
    console.log('[i18n] i18n.language is now:', i18n.language);
    try {
        AsyncStorage.setItem(STORE_LANGUAGE_KEY, lng);
    } catch (_) { }
}

export async function loadSavedLanguage(): Promise<string | null> {
    try {
        return await AsyncStorage.getItem(STORE_LANGUAGE_KEY);
    } catch (_) {
        return null;
    }
}

